def schedule_monthly_statements() -> None:
    """
    Generate owner statements for all properties.
    Runs on the 1st of each month at MONTHLY_STATEMENTS_TIME.
    """
    logger.info("=== Monthly Owner Statements: starting run ===")
    today = date.today()

    # Only run on the 1st — schedule lib fires daily at the given time,
    # so we gate on the calendar day
    if today.day != 1:
        logger.debug("Not the 1st of month (day=%d) — skipping statements", today.day)
        return

    try:
        from owner_statement import generate_all_statements

        output_subdir = os.path.join(OUTPUT_DIR, "owner_statements", today.strftime("%Y-%m"))
        results = generate_all_statements(output_dir=output_subdir)

        logger.info(
            "Monthly statements complete — %d statements generated in %s",
            len(results),
            output_subdir,
        )

        # Email each statement to the appropriate owner
        for result in results:
            owner_email = result.get("owner_email")
            pdf_path = result.get("pdf_path", "")
            if owner_email and pdf_path and os.path.exists(pdf_path):
                _deliver_report(
                    subject=f"Owner Statement — {result.get('property_name', 'Portfolio')} — {today.strftime('%B %Y')}",
                    pdf_path=pdf_path,
                    recipients=[owner_email, ALERT_EMAIL],
                )

    except ImportError as exc:
        logger.error("Could not import owner_statement module: %s", exc)
    except Exception as exc:
        logger.exception("Monthly owner statements failed: %s", exc)


def schedule_weekly_kpi() -> None:
    """
    Generate the weekly KPI dashboard.
    Runs every Monday at WEEKLY_KPI_TIME.
    """
    logger.info("=== Weekly KPI Dashboard: starting run ===")
    today = date.today()

    try:
        from kpi_dashboard import generate_kpi_dashboard

        output_subdir = os.path.join(OUTPUT_DIR, "kpi_dashboards")
        results = generate_kpi_dashboard(output_dir=output_subdir, persist=True)

        pdf_path = results.get("pdf_path", "")
        md_path = results.get("markdown_path", "")
        logger.info(
            "KPI dashboard complete — PDF: %s  Markdown: %s",
            pdf_path,
            md_path,
        )

        if pdf_path and os.path.exists(pdf_path):
            week_str = today.strftime("%B %d, %Y")
            _deliver_report(
                subject=f"Camelot KPI Dashboard — Week Ending {week_str}",
                pdf_path=pdf_path,
                recipients=[ALERT_EMAIL],
            )

    except ImportError as exc:
        logger.error("Could not import kpi_dashboard module: %s", exc)
    except Exception as exc:
        logger.exception("Weekly KPI dashboard failed: %s", exc)


def schedule_quarterly_update() -> None:
    """
    Generate the quarterly investor update.
    Runs on the 1st of Jan / Apr / Jul / Oct at QUARTERLY_UPDATE_TIME.
    """
    logger.info("=== Quarterly Investor Update: starting run ===")
    today = date.today()

    # Gate on: day=1 AND month in QUARTERLY_MONTHS
    if today.day != 1 or today.month not in QUARTERLY_MONTHS:
        logger.debug(
            "Not a quarterly trigger date (month=%d, day=%d) — skipping",
            today.month,
            today.day,
        )
        return

    # Determine which quarter *just ended* (prior quarter)
    # E.g. Jan 1 → report on Q4 of prior year
    quarter_map = {1: 4, 4: 1, 7: 2, 10: 3}
    report_quarter_num = quarter_map[today.month]
    report_year = today.year - 1 if today.month == 1 else today.year

    try:
        from investor_update import QuarterPeriod, generate_investor_update

        quarter = QuarterPeriod(year=report_year, quarter=report_quarter_num)
        output_subdir = os.path.join(OUTPUT_DIR, "investor_updates")
        pdf_path = generate_investor_update(quarter=quarter, output_dir=output_subdir)

        logger.info("Investor update complete — %s", pdf_path)

        if pdf_path and os.path.exists(pdf_path):
            _deliver_report(
                subject=f"Camelot Investor Update — {quarter.label}",
                pdf_path=pdf_path,
                recipients=[ALERT_EMAIL],
            )

    except ImportError as exc:
        logger.error("Could not import investor_update module: %s", exc)
    except Exception as exc:
        logger.exception("Quarterly investor update failed: %s", exc)


