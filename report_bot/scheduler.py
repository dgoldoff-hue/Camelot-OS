OUTPUT_DIR: str = os.getenv("REPORT_OUTPUT_DIR", "output/reports")
ALERT_EMAIL: str = os.getenv("REPORT_ALERT_EMAIL", "dgoldoff@camelot.nyc")
SEND_EMAIL_ON_COMPLETION: bool = os.getenv("REPORT_EMAIL_DELIVERY", "true").lower() == "true"
PID_FILE: str = os.getenv("SCHEDULER_PID_FILE", "/tmp/camelot_report_scheduler.pid")

# Cron times (24h, EST — ensure server TZ is set to America/New_York or
# adjust to UTC offsets in production)
MONTHLY_STATEMENTS_TIME: str = os.getenv("MONTHLY_STATEMENTS_TIME", "06:00")
WEEKLY_KPI_TIME: str = os.getenv("WEEKLY_KPI_TIME", "08:00")
QUARTERLY_UPDATE_TIME: str = os.getenv("QUARTERLY_UPDATE_TIME", "07:00")

# Months that trigger a quarterly investor update
QUARTERLY_MONTHS: frozenset[int] = frozenset([1, 4, 7, 10])


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _ensure_dirs() -> None:
    """Create required directories if they do not exist."""
    Path(OUTPUT_DIR).mkdir(parents=True, exist_ok=True)
    log_dir = Path(os.getenv("LOG_FILE", "logs/report_scheduler.log")).parent
    log_dir.mkdir(parents=True, exist_ok=True)


def _write_pid() -> None:
    """Write current PID to file for external process management."""
    try:
        with open(PID_FILE, "w") as f:
            f.write(str(os.getpid()))
        logger.info("PID file written to %s (PID=%d)", PID_FILE, os.getpid())
    except OSError as exc:
        logger.warning("Could not write PID file: %s", exc)


def _remove_pid() -> None:
    """Remove PID file on clean shutdown."""
    try:
        if os.path.exists(PID_FILE):
            os.remove(PID_FILE)
    except OSError:
        pass


def _deliver_report(subject: str, pdf_path: str, recipients: list[str]) -> None:
    """
    Email the generated report to recipients.
    Falls back gracefully if SMTP is not configured.
    """
    if not SEND_EMAIL_ON_COMPLETION:
        logger.info("Email delivery disabled — skipping send for: %s", pdf_path)
        return

    smtp_host = os.getenv("SMTP_HOST")
    if not smtp_host:
        logger.warning("SMTP_HOST not set — cannot deliver report by email")
        return

    import smtplib
    import ssl
    from email import encoders
    from email.mime.base import MIMEBase
    from email.mime.multipart import MIMEMultipart
    from email.mime.text import MIMEText

    smtp_port = int(os.getenv("SMTP_PORT", "587"))
    smtp_user = os.getenv("SMTP_USER", "")
    smtp_pass = os.getenv("SMTP_PASSWORD", "")
    sender = os.getenv("SMTP_FROM", smtp_user or "reports@camelot.nyc")

    msg = MIMEMultipart()
    msg["From"] = sender
    msg["To"] = ", ".join(recipients)
    msg["Subject"] = subject

    body = (
        f"Please find attached the {subject} generated by Camelot OS.\n\n"
        f"Report file: {os.path.basename(pdf_path)}\n"
        f"Generated at: {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}\n\n"
        "This is an automated message from Camelot OS Report Bot.\n"
        "Camelot Property Management Services Corp — Confidential."
    )
    msg.attach(MIMEText(body, "plain"))

    # Attach PDF
    try:
        with open(pdf_path, "rb") as f:
            part = MIMEBase("application", "octet-stream")
            part.set_payload(f.read())
        encoders.encode_base64(part)
        part.add_header(
            "Content-Disposition",
            f"attachment; filename={os.path.basename(pdf_path)}",
        )
        msg.attach(part)
    except OSError as exc:
        logger.error("Failed to attach PDF %s: %s", pdf_path, exc)
        return

    try:
        context = ssl.create_default_context()
        with smtplib.SMTP(smtp_host, smtp_port) as server:
            server.ehlo()
            server.starttls(context=context)
            if smtp_user and smtp_pass:
                server.login(smtp_user, smtp_pass)
            server.sendmail(sender, recipients, msg.as_string())
        logger.info("Report emailed to %s — %s", recipients, subject)
    except smtplib.SMTPException as exc:
        logger.error("SMTP send failed for %s: %s", subject, exc)


