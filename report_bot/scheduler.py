def _register_jobs() -> None:
    """Register all recurring jobs with the `schedule` library."""

    # Monthly owner statements — run check daily at the configured time
    # The function itself gates on day==1
    schedule.every().day.at(MONTHLY_STATEMENTS_TIME).do(schedule_monthly_statements)
    logger.info(
        "Registered: monthly owner statements at %s (fires daily, runs on 1st)",
        MONTHLY_STATEMENTS_TIME,
    )

    # Weekly KPI dashboard — every Monday
    schedule.every().monday.at(WEEKLY_KPI_TIME).do(schedule_weekly_kpi)
    logger.info(
        "Registered: weekly KPI dashboard on Mondays at %s",
        WEEKLY_KPI_TIME,
    )

    # Quarterly investor update — daily check, function gates on date
    schedule.every().day.at(QUARTERLY_UPDATE_TIME).do(schedule_quarterly_update)
    logger.info(
        "Registered: quarterly investor update check at %s (fires 1st of quarter months)",
        QUARTERLY_UPDATE_TIME,
    )


def _handle_signal(signum: int, frame: object) -> None:
    """Graceful shutdown on SIGTERM / SIGINT."""
    logger.info("Signal %d received — shutting down scheduler", signum)
    _remove_pid()
    sys.exit(0)


def run_daemon() -> None:
    """Start the scheduler daemon and block until interrupted."""
    _ensure_dirs()
    _write_pid()
    signal.signal(signal.SIGTERM, _handle_signal)
    signal.signal(signal.SIGINT, _handle_signal)

    _register_jobs()

    logger.info(
        "Camelot Report Scheduler started — %d jobs registered",
        len(schedule.jobs),
    )
    logger.info("Next runs:")
    for job in schedule.jobs:
        logger.info("  %s → next: %s", job.job_func.__name__, job.next_run)

    try:
        while True:
            schedule.run_pending()
            time.sleep(30)  # check every 30 seconds
    except (KeyboardInterrupt, SystemExit):
        logger.info("Scheduler stopped")
        _remove_pid()


# ---------------------------------------------------------------------------
# One-shot runners (for CLI / ad-hoc execution)
# ---------------------------------------------------------------------------

def run_owner_statements_now(month_date: Optional[date] = None) -> None:
    """Immediately generate owner statements for the given month (default: today)."""
    _ensure_dirs()

    target = month_date or date.today()

    try:
        from owner_statement import generate_all_statements

        output_subdir = os.path.join(OUTPUT_DIR, "owner_statements", target.strftime("%Y-%m"))
        results = generate_all_statements(output_dir=output_subdir)
        logger.info(
            "One-shot owner statements: %d generated in %s",
            len(results),
            output_subdir,
        )
        for r in results:
            print(f"  Statement: {r.get('pdf_path', '?')}")
    except Exception as exc:
        logger.exception("One-shot owner statements failed: %s", exc)
        sys.exit(1)


def run_kpi_dashboard_now() -> None:
    """Immediately generate the KPI dashboard."""
    _ensure_dirs()

    try:
        from kpi_dashboard import generate_kpi_dashboard

        output_subdir = os.path.join(OUTPUT_DIR, "kpi_dashboards")
        results = generate_kpi_dashboard(output_dir=output_subdir, persist=True)
        print(f"Markdown: {results['markdown_path']}")
        print(f"PDF:      {results['pdf_path']}")
    except Exception as exc:
        logger.exception("One-shot KPI dashboard failed: %s", exc)
        sys.exit(1)


def run_investor_update_now(year: Optional[int] = None, quarter_num: Optional[int] = None) -> None:
    """Immediately generate the investor update for the given or current quarter."""
    _ensure_dirs()

    try:
        from investor_update import QuarterPeriod, generate_investor_update

        q: Optional[QuarterPeriod] = None
        if year and quarter_num:
            q = QuarterPeriod(year=year, quarter=quarter_num)

        output_subdir = os.path.join(OUTPUT_DIR, "investor_updates")
        pdf_path = generate_investor_update(quarter=q, output_dir=output_subdir)
        print(f"PDF: {pdf_path}")
    except Exception as exc:
        logger.exception("One-shot investor update failed: %s", exc)
        sys.exit(1)


