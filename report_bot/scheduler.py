"""
scheduler.py — Camelot OS Report Bot
=====================================
Cron-based scheduler for automated report generation.

Schedule:
  - Monthly owner statements: 1st of each month at 06:00 EST
  - Weekly KPI dashboard:     Every Monday at 08:00 EST
  - Quarterly investor update: 1st of Jan / Apr / Jul / Oct at 07:00 EST

The scheduler uses the `schedule` library for in-process scheduling and
writes a PID file to allow external process management. It also supports
one-shot execution modes via CLI flags.

Usage:
    # Run the persistent scheduler daemon
    python scheduler.py --daemon

    # Run a specific report immediately (one-shot)
    python scheduler.py --run owner-statements
    python scheduler.py --run kpi-dashboard
    python scheduler.py --run investor-update [--year YYYY --quarter Q]

Author: Camelot OS
"""

